// EXEMPLO-01
// Function Padrão
function dobro (a) {
    return a * 2
}
console.log(dobro(2))

// Arrow Function
dobro2 = b => b * 2 // return implicito
console.log(dobro2(2))

//EXEMPLO-02

// Function padrão
let ola = function () {
    return "hello"
}
console.log(ola())

// Arrow Function
ola2 = () => "hello2"
console.log(ola2())

